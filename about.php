    <h1>
    	BACKGROUND
    </h1>
    <hr>
    <p><strong>“An international movement for developing human rights culture around the world through
academic excellence, technology and artificial intelligence.”</strong></p>
    <p>In 2010, the 1st International Conference on Human Rights Education (ICHRE) was held in
Western Sydney University, Australia. The conference examined the contribution of human
rights education (HRE) to furthering democracy, the rule of law, justice, and intercultural
dialogue and social harmony. This initiative has since evolved into a global grassroots movement
with annual international HRE conferences held in South Africa, Poland, Taiwan, USA, the
Netherlands, Chile, Canada and Australia.</p>
    <p>At the concluding session of the 9th ICHRE in Sydney it was unanimously agreed that the venue
for the next ICHRE should be Nepal. This was endorsed by the Sydney Declaration adopted at
the end of the event by all participants. The delegates from Nepal participating at the WSU
conference hall welcomed the idea and announced, in the midst of the applauding crowd of
nearly 350 participants from over 50 countries, that Nepal would be looking forward to
welcoming all in Kathmandu at the 10th ICHRE. </p>
    <p>Following a few rounds of high level meetings at Kathmandu University in Nepal and with the
visiting delegates from Western Sydney University led by Pro-Vice Chancellor Linda Taylor, it
was agreed that the 10th ICHRE be organized by KU in collaborative partnership with WSU in
cooperation with the Government of Nepal and other national and international partners
including civil society groups and academic institutions. Kathmandu University in joint
collaboration with Western Sydney University and with support of Australian Council for Human Rights Education is taking this opportunity to offer 10th ICHRE as
an appropriate forum to enhance human rights culture through academic excellence and
solidarity. Added to it would be an ideal blend of technology, artificial intelligence and rights in
academic and humanitarian dialogues and deliberations.
</p>
</br>
  <h1>AIM</h1>
  <hr>
    <p>The 10th International Conference on Human Rights Education coincides on the eve of Human
Rights Day and also Visit Nepal Year 2020.</p>
    <p>Much work has already been done to expand, diversify and improve knowledge and education
about these principles and practices of human rights globally in the larger interest of humanity
through ‘rule of law’ and ‘larger freedom from fear, want and security’. But there is still much
work to be done and embed these principles into everyday thinking where they can underpin
civil society and the underprivileged community. Academia is hitherto ignored and underused.</p>
    <p>Hence, in this conference, along with other important issues we look forward to explore the
potential of: Human Rights in Academic Excellence, Technology and Artificial Intelligence. </p>

</br>

<h1>GOAL</h1>
<hr>
<p>As a whole, the Conference will help the Nepali society, and other participating countries and
institutions, to internalise the values of human rights in fast growing technology and
environmental challenges, while providing the international community a better understanding of
global efforts and commitments towards protection and promotion of human rights, and work
together to counter the new challenges through promotion of human rights education. The
expectation is that the Conference will play its part not only in helping Nepal to set an example
to evolve a human rights friendly stable democracy under the rule of law and due process, which
will in turn help other participating scholars share their experiences in promoting human rights,
economic growth and social justice in other developing societies. It is in fact a partnership
building exercise through human rights education. </p>
</br>

<h1>OBJECTIVES</h1>
<hr>
<p>The Constitution of Nepal promulgated in 2015 expresses commitments to all major international
human rights treaties and gives high importance to the protection and promotion of human rights
in the new federal system and democratic environment. This conference with diverse national
and international participation has been planned for two-day long humanitarian exercise on 11-
12 December 2019, which will (a) take up the flavour of global solidarity in fostering a new
human rights culture, (b) be an assembly of human rights friendly people of different
nationalities coming from different social and cultural backgrounds, and (c) be a conclave for
working together and creating networks for a common humanitarian cause. </p>
<p>In this new world of opportunities and technological development, educational institutions of the
developing world have a greater role to play in maximizing the use of national capacity,
resources and advantages, from human rights perspective. The ICHRE 2019 will be a unique
forum to offer added opportunities for all to benefit from enrichment of human rights knowledge
and sharing of experiences.</p>
</br>

<h1>THEMES</h1>
<hr>
<p>The main theme of the 10th International Conference on Human Rights Education, is: <strong><h5>“Human Rights in Academic Excellence, Technology and Artificial Intelligence”.  </h5> </strong>  
Other important areas that will be covered as subthemes in line with the main theme include: 
</p>
<ul>
  <li><strong>Human Rights in academic Institutions;</strong> </br>
This includes recognition and use of academic research, technology and artificial intelligence as tools for ensuring collaboration in human rights, enrichment of knowledge, and economic prosperity for all.   
</li>
<li><strong>Human Rights in SDGs and Agenda 2030;</strong></li>
<li><strong>Human rights and culture;</strong></li>
<li><strong>Human rights in good governance, peace process and justice. </strong></li>
</ul>

</br>

<h1>PROGRAM AND OUTCOME</h1>
<hr>
<h4>Program:</h4>
<p>The Conference invites international participation seeking the attendance of educationists, academics, political representatives, analysts, bureaucrats, economists, international organizations, educational institutions, national human rights institutions, civil society organizations (CSOs) and leaders, medias and activists from Nepal, Asia and overseas. The Conference will be organised in Kathmandu from 11-12 December 2019</p>
<p>The conference will hold different plenary and thematic sessions, focal group discussions, working sessions and paper presentations (including the inaugural session on the morning of 11th December). Kathmandu University and Western Sydney University will jointly launch “International Centre for Human Rights”, and at the end the conference will adopt Kathmandu Declaration on HRE. Not counting the opening plenary, which will have a large number of additional invitees as audience, the Conference organisers are planning for about 200 participants for regular thematic sessions. Besides invited panelists, the Conference also welcomes independent scholars and interested individuals for participation. An excellent edited volume of human rights literature is expected to emerge from the Conference deliberations.
</p>
</br>
<h4>Outcomes:</h4>
<ul>
  <li>Academic Institutions’ active contribution to promote human rights and social justice through the networking and use of technology and artificial intelligence.</li>
  <li>This event will be very helpful to achieve UN’s SDG Goals and Agenda 2030 in the interest of global citizenship</li>
  <li>This event will also help to facilitate the Nepal Visit Year 2020.</li>
