  <h1 style="color: black;">About Western Sydney University</h1>
  <p>The University of Western Sydney (UWS) began operation on 1st January 1989, under the terms of the University of Western Sydney Act, 1988 which had been passed by the New South Wales Parliament in December 1988. However, the predecessors of the University date back as far as 1891 with the establishment of the Hawkesbury Agricultural College.</br>

The Act created a federated network university, based on two existing Colleges of Advanced Education - Hawkesbury Agricultural College and Nepean College of Advanced Education.</br>

Following incorporation into the University, the foundation network members were known as the 'University of Western Sydney, Hawkesbury' (UWS Hawkesbury) and the 'University of Western Sydney, Nepean' (UWS Nepean).</br>

The Act was amended by the University of Western Sydney (Amendment) Act, 1989 (Act No. 128, 1989) and the Macarthur Institute of Higher Education became the third University member on 1 November 1989. The new campus was known as the 'University of Western Sydney, Macarthur' (UWS Macarthur).</br>

In 1995 a review of the structure of UWS was undertaken. The Report of the Committee to Review the Structure of the University of Western Sydney (the Rogers Report) recommended restructuring the institution and a new federated University system emerged. The University of Western became a federated university system comprising four co-operative and interrelated elements: Office of the Vice-Chancellor, UWS Hawkesbury, UWS Macarthur, UWS Nepean.</br>

This federated system ensured the University was represented at a national and international level as a single institution with common objectives and values, while giving each of its Members the autonomy needed to react quickly and flexibly to the demands and needs of its local communities. The principal advantage of the federated network structure was the opportunity to build on the individual strengths of each member university, and through the University as a whole to define and achieve objectives that the individual members might have found unattainable. Each member was largely autonomous and responsible for: the development and conduct of courses; the admission of students; the initiation and supervision of research programs; staffing; the development of consultancy and entrepreneurial activities; and the development and maintenance of campus facilities and properties.</br>

On 26 November 1997, the University of Western Sydney Act, 1997 (Act No. 116, 1997) was passed by the State Parliament. This Act which replaced the original Act came into force on 1 January 1998.</br>

Following consultation across UWS the Vice Chancellor put proposals before the UWS Board of Trustees at the end of 1999 for a major restructure of the University. The Board approved the principles of the restructure, and the consequent detailed work to be undertaken to develop the new structure. From the beginning of 2001 the University of Western Sydney operated as a single multi-campus university rather than as a federation.</br>

The new structure of the university was outlined in the 2001 Calendar with revisions and changes outlined in the 2002 Calendar.</br>

The single multi-campus University of Western Sydney has six campuses: Bankstown, Blacktown, Campbelltown, Hawkesbury, Parramatta, and Penrith.</br></p>

